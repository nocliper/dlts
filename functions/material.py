def material(name):
    import numpy as np

    if name == 'Ga2O3':
        m0  = 9.10938356*10**-31#kg
        m_h = 3*m0
        m_e = 0.28*m0
        k   = 1.3806485279*10**-23#J/K
        h   = 6.62607004081*10**-34#J*s

        A_e = (3*k/m_e)**0.5*100*2*((2*np.pi*m_e*k)/(h**2))**(3/2)*100**-3 ##(Nc*v_th)/T**2 #cm**-3/K**2
        #print('A_e = %.2e'%A_e)
        A_h = (3*k/m_h)**0.5*100*2*((2*np.pi*m_h*k)/(h**2))**(3/2)*100**-3 ##(Nc*v_th)/T**2 #cm**-3/K**2
        #print('A_h = %.2e'%A_h)
    elif name == 'GaN':
        m0  = 9.10938356*10**-31#kg
        m_h = 0.3*m0
        m_e = 0.2*m0
        k   = 1.3806485279*10**-23#J/K
        h   = 6.62607004081*10**-34#J*s

        A_e = (3*k/m_e)**0.5*100*2*((2*np.pi*m_e*k)/(h**2))**(3/2)*100**-3 ##(Nc*v_th)/T**2 #cm**-3/K**2
        #print('A_e = %.2e'%A_e)
        A_h = (3*k/m_h)**0.5*100*2*((2*np.pi*m_h*k)/(h**2))**(3/2)*100**-3 ##(Nc*v_th)/T**2 #cm**-3/K**2
        #print('A_h = %.2e'%A_h)

    return A_e, A_h
